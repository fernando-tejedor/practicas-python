cadena='casa de madera'
print(len(cadena))#longitud: cuenta los caracteres de la cadena
print(cadena[2]) #imprimir posicion 2
print(cadena[8]) #imprimir posicion 8
print(cadena[0:4])#imprimir desde la posicion 0 hasta el caracter 4
print(cadena[5:7])#primer parametro= posicion inicial inicia en 0
                    #segundo parametro caracter an que desea llegar inicia en 1

print(cadena[0:])#se imprime de la inicial 0 o la definida hasta el final
print(cadena[:5])#se imprime desde el inicio hasta el caracter 5 o el definido
